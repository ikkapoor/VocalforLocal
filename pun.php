                The Punjabi term, <b>Phulkari </b>is made of two wors: Phul means flower and Akari means shape. Phulkari meant the shape/direction of flowers which symbolized life. In Punjab, it was popularly believed that the birth of girl child in the family was auspicious.<br> 
                 phulkari suits : Phulkari (Punjabi: ਫੁਲਕਾਰੀ) refers to the folk embroidery of the Punjab. ... The main characteristics of Phulkari embroidery are use of darn stitch on the wrong side of coarse cotton cloth with coloured silken thread.<br>
                Traditional phulkari embroidery is done on coarse cotton fabric called 'khaddar' which is manually spun and naturally dyed. The coarse weave makes it easy to run the thread to it in order to make the intricate designs. The thread that is used for the embroidery is called 'pat' in Punjabi.    >
                Spun from the charkha this spectacular style of embroidery is patterned on odinis, shawls, kurtis and chunris. The main characteristics of this embroidery are the use of darn stitch on the wrong side of cloth with colored silken thread. <br>A face of fashion that finds its first mentions in Punjabi folklore of the romantic protagonists Heer and Ranjha, Phulkari is a dream weaver for every Punjabi girl.


