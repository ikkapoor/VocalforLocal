                Caps, <b>the glorious crown of Himachal Pradesh </b>are now a recognized fashion logo in every hue. Colorful caps fascinate everyone at first instance.

                            The traditional yet stylish headgear has derived its name from the place of its origin. Locally, it is called as Kullu topi, Malana Topi, Kinnauri Topi, Bushehri Topi and is an important part of a local man’s attire/apparel/garments. Alike Kullu district, in some of the other districts also, cap is an important part of male’s and Female’s attire.
                            <br><br>
                The designs of the caps represent the area. The Kullu cap is round in shape and is flat on the top. A band of colorful border brightens the front with beautiful pattern, which is separately woven on small looms and the back portion, which covers the head is made of local woolen yarn and sometimes is also made of cotton or any other light material.
                <br>
                These days, plain velvet caps in maroon and green colors are also available in the market. Once use of bright colors in the cap was the significant feature of the caps but nowadays, vegetable colours are being used.
                <br>
                These caps are categorized as small, medium and large. The price of a Kullu cap depends on the cloth used and the pattern on the border

